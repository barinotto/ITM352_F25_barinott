# Tuples
celebrities_tuple = ("Taylor Swift", "Lionel Messi", "Max Verstappen", "Keanu Reeves", "Angelina Jolie")
ages_tuple = (35, 37, 27, 60, 49)

# Convert tuples to lists directly
celebrities_list = list(celebrities_tuple)
ages_list = list(ages_tuple)

# Create the dictionary
celebrity_dict = {
    "celebrities": celebrities_list,
    "ages": ages_list
}

print(celebrity_dict)